const Authors = ({ show }: { show: boolean }) => {
  if (!show) {
    return null
  }
  const authors = [] as Author[]

  return (
    <div>
      <h2>authors</h2>
      <table>
        <tbody>
          <tr>
            <th></th>
            <th>born</th>
            <th>books</th>
          </tr>
          {authors.map((a) => (
            <tr key={a.name}>
              <td>{a.name}</td>
              <td>{a.born}</td>
              <td>{a.bookCount}</td>
            </tr>
          ))}
        </tbody>
      </table>
    </div>
  )
}

type Author = { name: string; born?: number; bookCount: number }

export default Authors
